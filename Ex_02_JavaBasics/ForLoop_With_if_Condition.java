package Ex_02_JavaBasics;

public class ForLoop_With_if_Condition {
    public static void main(String[] args) {
        for (int i = 0; i <= 18; i++) {
            if(i < 18) {
                System.out.println("No, Vote is not granted");

            }else
                System.out.println("Yes, Vote is granted");
        }
    }
}
