package Ex_02_JavaBasics;

public class For_Loop {
    public static void main(String[] args) {
        // Repeat block of code until condition is false
        //Syntax - Initialization - Condition -Update [ Increment/Decrement
        //ICU
        // for(I:C:U)
        for(int i=0 ; i<10 ; i++){
            System.out.println(i);
        }
    }
}
